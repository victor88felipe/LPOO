        private void carregarListaAgenda() {
        AgendaDAO agendaDAO = new AgendaDAO();
        
        List<AgendaDTO> listaagenda = new ArrayList<>();
        listaagenda = agendaDAO.listarAgendas();       

     
        DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nome");


        if (listaagenda.size() == 0) {
            modelo.addRow(new String[]{"Sem dados",
                        null});
        }
        
        for (int i = 0; i < listaagenda.size(); i++) {
            AgendaDTO p = listaagenda.get(i);

            // Alimenta as linhas de dados  
            modelo.addRow(new Object[]{
                        p.getIdAgenda(),
                        p.getNomeAgenda()});
        }

        tabelaListaAgenda.setModel(modelo);

    }

