    private void botaoSalvarAgendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarAgendaActionPerformed
        // TODO add your handling code here:
        String nome = this.nomeAgenda.getText();
       
        if (this.agendas != null){
        AgendaDTO agendaDTO = new AgendaDTO();
        AgendaDAO agendaDAO = new AgendaDAO();
        agendaDTO.setIdAgenda(this.agendas.getIdAgenda());
        agendaDTO.setNomeAgenda(nome);
        System.out.println(nome);
        System.out.println(this.agendas.getIdAgenda()); 
        agendaDAO.atualizar(agendaDTO);
        JOptionPane.showMessageDialog(null, 
                    "Cadastro Atualizado com sucesso", 
                    "Cadastro", JOptionPane.PLAIN_MESSAGE);
        this.agendas = null;
        carregarListaAgenda();
        } else {
          System.out.println(nome);
            AgendaDTO agendaDTO = new AgendaDTO();
            AgendaDAO agendaDAO = new AgendaDAO();
            agendaDTO.getIdAgenda();
            agendaDTO.setNomeAgenda(nome);
            agendaDAO.inserir(agendaDTO);
            JOptionPane.showMessageDialog(null, 
                    "Cadastro sALVO com sucesso", 
                    "Cadastro", JOptionPane.PLAIN_MESSAGE);   
            
            carregarListaAgenda();
        }
        
    }//GEN-LAST:event_botaoSalvarAgendaActionPerformed

    private void botaoExcluirAgendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirAgendaActionPerformed
        // TODO add your handling code here:
        try{
            AgendaDAO agenda = new AgendaDAO();
            int linha = tabelaListaAgenda.getSelectedRow();   
            int coluna = tabelaListaAgenda.getColumnModel().getColumnIndex("ID"); 
            Object value = tabelaListaAgenda.getValueAt(linha, coluna);  
            Integer numero = (Integer)value;
            System.out.println(numero);
            agenda.remover(numero);
                    
        carregarListaAgenda();
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Selecione uma Agenda!");
        }
    }//GEN-LAST:event_botaoExcluirAgendaActionPerformed

